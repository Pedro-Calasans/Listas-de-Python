op=input("Digite a operação")
if op== '+':
    num1,num2=int(input("N1:\n")), int(input("N2:\n"))
    print(f"A soma é igual a {num1+num2}")
elif op=="-":
    num1,num2=int(input("N1:\n")), int(input("N2:\n"))
    print(f"A subtração é igual a {num1-num2}")
elif op=="*":
    num1,num2=int(input("N1:\n")), int(input("N2:\n"))
    print(f"A multiplicação é igual a {num1*num2}")
elif op=="/":
    num1,num2=int(input("N1:\n")), int(input("N2:\n"))
    print(f"A divisão é igual a {num1/num2}")
